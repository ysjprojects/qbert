class QBertEmbeddings(nn.Module):
    """Construct the embeddings from word, position and token_type embeddings."""

    def __init__(self, config):
        super().__init__()
        self.word_embeddings = nn.Embedding(config.vocab_size, config.hidden_size, padding_idx=config.pad_token_id)
        self.position_embeddings = nn.Embedding(config.max_position_embeddings, config.hidden_size)
        self.token_type_embeddings = nn.Embedding(config.type_vocab_size, config.hidden_size)

        # self.LayerNorm is not snake-cased to stick with TensorFlow model variable name and be able to load
        # any TensorFlow checkpoint file
        self.LayerNorm = nn.LayerNorm(config.hidden_size, eps=config.layer_norm_eps)
        self.dropout = nn.Dropout(config.hidden_dropout_prob)
        # position_ids (1, len position emb) is contiguous in memory and exported when serialized
        self.position_embedding_type = getattr(config, "position_embedding_type", "absolute")
        self.register_buffer(
            "position_ids", torch.arange(config.max_position_embeddings).expand((1, -1)), persistent=False
        )
        self.register_buffer(
            "token_type_ids", torch.zeros(self.position_ids.size(), dtype=torch.long), persistent=False
        )

    def forward(
        self,
        input_ids: Optional[torch.LongTensor] = None,
        token_type_ids: Optional[torch.LongTensor] = None,
        position_ids: Optional[torch.LongTensor] = None,
        inputs_embeds: Optional[torch.FloatTensor] = None,
        past_key_values_length: int = 0,
    ) -> torch.Tensor:
        if input_ids is not None:
            input_shape = input_ids.size()
        else:
            input_shape = inputs_embeds.size()[:-1]

        seq_length = input_shape[1]

        if position_ids is None:
            position_ids = self.position_ids[:, past_key_values_length : seq_length + past_key_values_length]

        # Setting the token_type_ids to the registered buffer in constructor where it is all zeros, which usually occurs
        # when its auto-generated, registered buffer helps users when tracing the model without passing token_type_ids, solves
        # issue #5664
        if token_type_ids is None:
            if hasattr(self, "token_type_ids"):
                buffered_token_type_ids = self.token_type_ids[:, :seq_length]
                buffered_token_type_ids_expanded = buffered_token_type_ids.expand(input_shape[0], seq_length)
                token_type_ids = buffered_token_type_ids_expanded
            else:
                token_type_ids = torch.zeros(input_shape, dtype=torch.long, device=self.position_ids.device)

        if inputs_embeds is None:
            inputs_embeds = self.word_embeddings(input_ids)
        token_type_embeddings = self.token_type_embeddings(token_type_ids)

        embeddings = inputs_embeds + token_type_embeddings
        if self.position_embedding_type == "absolute":
            position_embeddings = self.position_embeddings(position_ids)
            embeddings += position_embeddings
        embeddings = self.LayerNorm(embeddings)
        embeddings = self.dropout(embeddings)
        return embeddings
    
class Quaternion:
    """
    A class to represent and operate on quaternions.
    Supports initialization, Hamilton product, addition, normalization, and conjugation.
    """

    def __init__(self, r=None, i=None, j=None, k=None, tensor=None):
        """
        Initialize with a tensor that will be split into quaternion components.
        The tensor's last dimension is assumed to represent concatenated quaternion components
        (r, i, j, k), with its size divisible by 4.

        Args:
            tensor (torch.Tensor): A tensor with quaternion components concatenated along the last dimension.
        """
        if tensor is not None:
            self.r, self.i, self.j, self.k = torch.split(tensor, tensor.shape[-1] // 4, dim=-1)
        else:
            assert r is not None and i is not None and j is not None and k is not None
            self.r = r
            self.i = i
            self.j = j
            self.k = k

    def hamilton_product(self, other, as_tensor=True, bias=None):
        """
        Perform Hamilton product (quaternion multiplication) between this quaternion
        and another quaternion using either element-wise or matrix multiplication.

        Args:
            other (Quaternion): Another quaternion.

        Returns:
            torch.Tensor: A tensor representing the resulting quaternion components (r, i, j, k).
        """
        r1, i1, j1, k1 = self.r, self.i, self.j, self.k
        r2, i2, j2, k2 = other.r, other.i, other.j, other.k
        
        rb, ib, jb, kb = 0, 0, 0, 0
        if bias is not None:
            rb = bias.r
            ib = bias.i
            jb = bias.j
            kb = bias.k

        if r1.dim() == 2 and r2.dim() == 2 and r1.shape[-1] == r2.shape[-2]:
            # Matrix multiplication
            r = torch.matmul(r1, r2) - torch.matmul(i1, i2) - torch.matmul(j1, j2) - torch.matmul(k1, k2) + rb
            i = torch.matmul(r1, i2) + torch.matmul(i1, r2) + torch.matmul(j1, k2) - torch.matmul(k1, j2) + ib
            j = torch.matmul(r1, j2) - torch.matmul(i1, k2) + torch.matmul(j1, r2) + torch.matmul(k1, i2) + jb
            k = torch.matmul(r1, k2) + torch.matmul(i1, j2) - torch.matmul(j1, i2) + torch.matmul(k1, r2) + kb
        else:
            # Element-wise multiplication
            r = r1 * r2 - i1 * i2 - j1 * j2 - k1 * k2 + rb
            i = r1 * i2 + i1 * r2 + j1 * k2 - k1 * j2 + ib
            j = r1 * j2 - i1 * k2 + j1 * r2 + k1 * i2 + jb
            k = r1 * k2 + i1 * j2 - j1 * i2 + k1 * r2 + kb

        q = Quaternion(r, i, j, k)
        if as_tensor:
            return q.as_tensor()
        return q

    def add(self, other):
        """
        Perform component-wise addition between two quaternions.

        Args:
            other (Quaternion): Another quaternion.

        Returns:
            Quaternion: A quaternion representing the sum.
        """
        r = self.r + other.r
        i = self.i + other.i
        j = self.j + other.j
        k = self.k + other.k

        return Quaternion(r, i, j, k)

    def normalize(self):
        """
        Normalize the quaternion to have unit norm.

        Returns:
            Quaternion: A normalized quaternion.
        """
        norm = torch.sqrt(self.r ** 2 + self.i ** 2 + self.j ** 2 + self.k ** 2)
        return Quaternion(self.r / norm, self.i / norm, self.j / norm, self.k / norm)

    def conjugate(self):
        """
        Return the conjugate of the quaternion: (r, -i, -j, -k).

        Returns:
            Quaternion: The conjugated quaternion.
        """
        return Quaternion(self.r, -self.i, -self.j, -self.k)

    def as_tensor(self):
        """
        Return the quaternion as a single concatenated tensor (r, i, j, k components).
        If the tensor is 2D, unsqueeze it to add a batch dimension of 1.

        Returns:
            torch.Tensor: The concatenated quaternion tensor.
        """
        tensor = torch.cat([self.r, self.i, self.j, self.k], dim=-1)
        if tensor.ndim == 2:
            tensor = tensor.unsqueeze(0)
        return tensor

class QuaternionTransformation(nn.Module):
    """
    A PyTorch module for applying a quaternion transformation to input data using
    learnable quaternion weights. Supports optional activation functions.
    """

    def __init__(self, input_dim, output_dim, bias=True, device=None, dtype=None):
        """
        Initialize the QuaternionTransformation module.

        Args:
            input_dim (int): Dimensionality of the input tensor (must be divisible by 4).
            output_dim (int): Desired output dimensionality (number of quaternion features).
            activation (callable, optional): Activation function to apply after transformation.
            init (callable, optional): Initialization function for quaternion weights. Defaults to Xavier uniform.
        """
        factory_kwargs = {'device': device, 'dtype': dtype}
        super().__init__()

        # Quaternion input dimension is divided by 4 (for r, i, j, k components)
        assert input_dim % 4 == 0, "Input dimension must be divisible by 4"
        assert output_dim % 4 == 0, "Output dimension must be divisible by 4"

        self.input_dim = input_dim // 4
        self.output_dim = output_dim // 4

        # Learnable quaternion weight matrices for r, i, j, k components
        self.r_weight = nn.Parameter(torch.empty(self.input_dim, self.output_dim, **factory_kwargs))
        self.i_weight = nn.Parameter(torch.empty(self.input_dim, self.output_dim, **factory_kwargs))
        self.j_weight = nn.Parameter(torch.empty(self.input_dim, self.output_dim, **factory_kwargs))
        self.k_weight = nn.Parameter(torch.empty(self.input_dim, self.output_dim, **factory_kwargs))

        if bias:
            self.r_bias = nn.Parameter(torch.empty(output_dim, **factory_kwargs))
            self.i_bias = nn.Parameter(torch.empty(output_dim, **factory_kwargs))
            self.j_bias = nn.Parameter(torch.empty(output_dim, **factory_kwargs))
            self.k_bias = nn.Parameter(torch.empty(output_dim, **factory_kwargs))
        else:
            self.register_parameter('r_bias', None)
            self.register_parameter('i_bias', None)
            self.register_parameter('j_bias', None)
            self.register_parameter('k_bias', None)

        self.reset_parameters()

    def reset_parameters(self):
        nn.init.kaiming_uniform_(self.r_weight, a=math.sqrt(5))
        nn.init.kaiming_uniform_(self.i_weight, a=math.sqrt(5))
        nn.init.kaiming_uniform_(self.j_weight, a=math.sqrt(5))
        nn.init.kaiming_uniform_(self.k_weight, a=math.sqrt(5))

        if self.r_bias is not None:
            fan_in, _ = nn.init._calculate_fan_in_and_fan_out(self.r_weight)
            bound = 1 / math.sqrt(fan_in) if fan_in > 0 else 0
            nn.init.uniform_(self.r_bias, -bound, bound)
        if self.i_bias is not None:
            fan_in, _ = nn.init._calculate_fan_in_and_fan_out(self.i_weight)
            bound = 1 / math.sqrt(fan_in) if fan_in > 0 else 0
            nn.init.uniform_(self.i_bias, -bound, bound)
        if self.j_bias is not None:
            fan_in, _ = nn.init._calculate_fan_in_and_fan_out(self.j_weight)
            bound = 1 / math.sqrt(fan_in) if fan_in > 0 else 0
            nn.init.uniform_(self.j_bias, -bound, bound)
        if self.k_bias is not None:
            fan_in, _ = nn.init._calculate_fan_in_and_fan_out(self.k_weight)
            bound = 1 / math.sqrt(fan_in) if fan_in > 0 else 0
            nn.init.uniform_(self.k_bias, -bound, bound)

    def forward(self, x):
        """
        Perform the quaternion transformation on the input tensor.

        Args:
            x (torch.Tensor): Input tensor of shape [batch_size, seq_len, input_dim] (3D)
                              or [batch_size, input_dim] (2D). The last dimension must
                              be divisible by 4 for quaternion processing.

        Returns:
            torch.Tensor: Output tensor of shape [batch_size, seq_len, output_dim * 4] (for 3D input)
                          or [batch_size, output_dim * 4] (for 2D input), representing
                          the transformed quaternion components (r, i, j, k).
        """
        is_3d_input = x.ndim == 3  # Check if the input is 3D

        if is_3d_input:
            # Flatten 3D input to 2D for processing
            batch_size, seq_len, output_dim = x.shape
            x = x.view(batch_size * seq_len, output_dim)

        # Convert input tensor into quaternion components (r, i, j, k)
        q_x = Quaternion(tensor=x)
        q_bias = Quaternion(r=self.r_bias, i=self.i_bias, j=self.j_bias, k=self.k_bias)

        # Create a quaternion from the learnable weights
        q_kernel = Quaternion(self.r_weight, self.i_weight, self.j_weight, self.k_weight)

        # Perform Hamilton product (quaternion multiplication)
        output = q_x.hamilton_product(q_kernel, bias=q_bias)

        if is_3d_input:
            # Reshape back to 3D if input was originally 3D
            return output.view(batch_size, seq_len, self.output_dim * 4)

        return output

class QBertSelfAttention(nn.Module):
    def __init__(self, config, position_embedding_type=None):
        super().__init__()
        if config.hidden_size % config.num_attention_heads != 0 and not hasattr(config, "embedding_size"):
            raise ValueError(
                f"The hidden size ({config.hidden_size}) is not a multiple of the number of attention "
                f"heads ({config.num_attention_heads})"
            )
        
        if config.hidden_size % 4 != 0:
            raise ValueError(
                f"The hidden size ({config.hidden_size}) is not a multiple of 4"
            )

        self.num_attention_heads = config.num_attention_heads
        self.attention_head_size = int(config.hidden_size / config.num_attention_heads)
        if self.attention_head_size % 4 != 0:
            raise ValueError(
                f"The attention head size ({self.attention_head_size}) is not a multiple of 4"
            )
        self.all_head_size = self.num_attention_heads * self.attention_head_size

        self.query = QuaternionTransformation(config.hidden_size, self.all_head_size)
        self.key = QuaternionTransformation(config.hidden_size, self.all_head_size)
        self.value = QuaternionTransformation(config.hidden_size, self.all_head_size)

        self.dropout = nn.Dropout(config.attention_probs_dropout_prob)

    def transpose_for_scores(self, x: torch.Tensor) -> torch.Tensor:
        new_x_shape = x.size()[:-1] + (self.num_attention_heads, self.attention_head_size)
        x = x.view(new_x_shape)
        return x.permute(0, 2, 1, 3)
    
    def quaternion_attention(self, a, b):
        """
        Perform dot product attention between two quaternion sequences.

        Args:
            a (torch.Tensor): The first quaternion tensor of shape [batch_size, seq_len_a, dim].
            b (torch.Tensor): The second quaternion tensor of shape [batch_size, seq_len_b, dim].

        Returns:
            Quaternion: A quaternion object containing attention matrices for each quaternion component (r, i, j, k).

        Note:
            The quaternion attention formula is based on Hamilton product:
            (rr' - xx' - yy' - zz')  +
            (rx' + xr' + yz' - zy')i +
            (ry' - xz' + yr' + zx')j +
            (rz' + xy' - yx' + zr')k
        """
        # Ensure the input dimensions are divisible by 4
        assert a.size(-1) % 4 == 0, "Last dimension of input tensor must be divisible by 4 for quaternion representation."
        assert b.size(-1) % 4 == 0, "Last dimension of input tensor must be divisible by 4 for quaternion representation."

        # Split inputs into quaternion components
        ar, ai, aj, ak = torch.chunk(a, 4, dim=-1)
        br, bi, bj, bk = torch.chunk(b, 4, dim=-1)
        br = br.transpose(-1, -2)
        bi = bi.transpose(-1, -2)
        bj = bj.transpose(-1, -2)
        bk = bk.transpose(-1, -2)

        # Compute the quaternion Hamilton product
        r = (torch.matmul(ar, br)
             - torch.matmul(ai, bi)
             - torch.matmul(aj, bj)
             - torch.matmul(ak, bk))
        i = (torch.matmul(ar, bi)
             + torch.matmul(ai, br)
             + torch.matmul(aj, bk)
             - torch.matmul(ak, bj))
        j = (torch.matmul(ar, bj)
             - torch.matmul(ai, bk)
             + torch.matmul(aj, br)
             + torch.matmul(ak, bi))
        k = (torch.matmul(ar, bk)
             + torch.matmul(ai, bj)
             - torch.matmul(aj, bi)
             + torch.matmul(ak, br))

        return Quaternion(r,i,j,k)

    def forward(
        self,
        hidden_states: torch.Tensor,
        attention_mask: Optional[torch.FloatTensor] = None,
        head_mask: Optional[torch.FloatTensor] = None,
        encoder_hidden_states: Optional[torch.FloatTensor] = None,
        encoder_attention_mask: Optional[torch.FloatTensor] = None,
        output_attentions: Optional[bool] = False,
    ) -> Tuple[torch.Tensor]:
        query_layer = self.query(hidden_states)
        key_layer = self.key(hidden_states)
        value_layer = self.value(hidden_states)

        query_layer = self.transpose_for_scores(query_layer)
        key_layer = self.transpose_for_scores(key_layer)
        value_layer = self.transpose_for_scores(value_layer)
        
        attention_scores = self.quaternion_attention(query_layer, key_layer)

        attention_norm = torch.sqrt(
            attention_scores.r**2 + 
            attention_scores.i**2 + 
            attention_scores.j**2 + 
            attention_scores.k**2 + 
            1e-8
        )

        attention_norm = attention_norm / math.sqrt(self.attention_head_size / 4)
        
        
        if attention_mask is not None:
            attention_norm = attention_norm + attention_mask
            
        attention_weights = nn.functional.softmax(attention_norm, dim=-1)

        # This is actually dropping out entire tokens to attend to, which might
        # seem a bit unusual, but is taken from the original Transformer paper.
        attention_weights = self.dropout(attention_weights)

        # Mask heads if we want to
        if head_mask is not None:
            attention_weights = attention_weights * head_mask

        value_r, value_i, value_j, value_k = torch.chunk(value_layer, 4, dim=-1)

        context_r = torch.matmul(attention_weights, value_r)
        context_i = torch.matmul(attention_weights, value_i)
        context_j = torch.matmul(attention_weights, value_j)
        context_k = torch.matmul(attention_weights, value_k)

        context_layer = torch.cat((context_r, context_i, context_j, context_k), dim=-1)

        context_layer = context_layer.permute(0, 2, 1, 3).contiguous()  # Now [batch_size, seq_len, num_heads, head_dim]
        new_context_layer_shape = context_layer.size()[:-2] + (self.all_head_size,)  # Flatten last two dims
        context_layer = context_layer.view(new_context_layer_shape)

        outputs = (context_layer, attention_weights) if output_attentions else (context_layer,)
        
        return outputs


class QBertSelfOutput(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = nn.Linear(config.hidden_size, config.hidden_size)
        self.LayerNorm = nn.LayerNorm(config.hidden_size, eps=config.layer_norm_eps)
        self.dropout = nn.Dropout(config.hidden_dropout_prob)

    def forward(self, hidden_states: torch.Tensor, input_tensor: torch.Tensor) -> torch.Tensor:
        hidden_states = self.dense(hidden_states)
        hidden_states = self.dropout(hidden_states)
        hidden_states = self.LayerNorm(hidden_states + input_tensor)
        return hidden_states


QBERT_SELF_ATTENTION_CLASSES = {
    "eager": QBertSelfAttention,
}


class QBertAttention(nn.Module):
    def __init__(self, config, position_embedding_type=None):
        super().__init__()
        self.self = QBERT_SELF_ATTENTION_CLASSES[config._attn_implementation](
            config, position_embedding_type=position_embedding_type
        )
        self.output = QBertSelfOutput(config)

    def forward(
        self,
        hidden_states: torch.Tensor,
        attention_mask: Optional[torch.FloatTensor] = None,
        head_mask: Optional[torch.FloatTensor] = None,
        encoder_hidden_states: Optional[torch.FloatTensor] = None,
        encoder_attention_mask: Optional[torch.FloatTensor] = None,
        output_attentions: Optional[bool] = False,
    ) -> Tuple[torch.Tensor]:
        self_outputs = self.self(
            hidden_states,
            attention_mask,
            head_mask,
            encoder_hidden_states,
            encoder_attention_mask,
            output_attentions,
        )
        attention_output = self.output(self_outputs[0], hidden_states)
        outputs = (attention_output,) + self_outputs[1:]  # add attentions if we output them
        return outputs


class QBertIntermediate(nn.Module):
    def __init__(self, config):
        super().__init__()
        #self.dense = nn.Linear(config.hidden_size, config.intermediate_size)
        self.dense = (
            QuaternionTransformation(config.hidden_size, config.intermediate_size)
            if config.quaternion_mode == "all"
            else nn.Linear(config.hidden_size, config.intermediate_size)
        )
        if isinstance(config.hidden_act, str):
            self.intermediate_act_fn = ACT2FN[config.hidden_act]
        else:
            self.intermediate_act_fn = config.hidden_act

    def forward(self, hidden_states: torch.Tensor) -> torch.Tensor:
        hidden_states = self.dense(hidden_states)
        hidden_states = self.intermediate_act_fn(hidden_states)
        return hidden_states


class QBertOutput(nn.Module):
    def __init__(self, config):
        super().__init__()

        self.dense = (
            QuaternionTransformation(config.intermediate_size, config.hidden_size)
            if config.quaternion_mode == "all"
            else nn.Linear(config.intermediate_size, config.hidden_size)
        )

        #self.dense = nn.Linear(config.intermediate_size, config.hidden_size)
        self.LayerNorm = nn.LayerNorm(config.hidden_size, eps=config.layer_norm_eps)
        self.dropout = nn.Dropout(config.hidden_dropout_prob)

    def forward(self, hidden_states: torch.Tensor, input_tensor: torch.Tensor) -> torch.Tensor:
        hidden_states = self.dense(hidden_states)
        hidden_states = self.dropout(hidden_states)
        hidden_states = self.LayerNorm(hidden_states + input_tensor)
        return hidden_states

class QBertLayer(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.chunk_size_feed_forward = config.chunk_size_feed_forward
        self.seq_len_dim = 1
        self.attention = QBertAttention(config)               
        self.intermediate = QBertIntermediate(config)
        self.output = QBertOutput(config)

    def forward(
        self,
        hidden_states: torch.Tensor,
        attention_mask: Optional[torch.FloatTensor] = None,
        head_mask: Optional[torch.FloatTensor] = None,
        encoder_hidden_states: Optional[torch.FloatTensor] = None,
        encoder_attention_mask: Optional[torch.FloatTensor] = None,
        output_attentions: Optional[bool] = False,
    ) -> Tuple[torch.Tensor]:
        self_attention_outputs = self.attention(
            hidden_states,
            attention_mask,
            head_mask,
            output_attentions=output_attentions,
        )
        attention_output = self_attention_outputs[0]

        outputs = self_attention_outputs[1:]  # add self attentions if we output attention weights

        layer_output = apply_chunking_to_forward(
            self.feed_forward_chunk, self.chunk_size_feed_forward, self.seq_len_dim, attention_output
        )
        outputs = (layer_output,) + outputs

        return outputs

    def feed_forward_chunk(self, attention_output):
        intermediate_output = self.intermediate(attention_output)
        layer_output = self.output(intermediate_output, attention_output)
        return layer_output


class QBertEncoder(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.config = config
        self.layer = nn.ModuleList([QBertLayer(config) for _ in range(config.num_hidden_layers)])
        self.gradient_checkpointing = False

    def forward(
        self,
        hidden_states: torch.Tensor,
        attention_mask: Optional[torch.FloatTensor] = None,
        head_mask: Optional[torch.FloatTensor] = None,
        encoder_hidden_states: Optional[torch.FloatTensor] = None,
        encoder_attention_mask: Optional[torch.FloatTensor] = None,
        output_attentions: Optional[bool] = False,
        output_hidden_states: Optional[bool] = False,
        return_dict: Optional[bool] = True,
    ) -> Union[Tuple[torch.Tensor], BaseModelOutputWithPastAndCrossAttentions]:
        all_hidden_states = () if output_hidden_states else None
        all_self_attentions = () if output_attentions else None

        for i, layer_module in enumerate(self.layer):
            if output_hidden_states:
                all_hidden_states = all_hidden_states + (hidden_states,)

            layer_head_mask = head_mask[i] if head_mask is not None else None

            if self.gradient_checkpointing and self.training:
                layer_outputs = self._gradient_checkpointing_func(
                    layer_module.__call__,
                    hidden_states,
                    attention_mask,
                    layer_head_mask,
                    encoder_hidden_states,
                    encoder_attention_mask,
                    output_attentions,
                )
            else:
                layer_outputs = layer_module(
                    hidden_states,
                    attention_mask,
                    layer_head_mask,
                    encoder_hidden_states,
                    encoder_attention_mask,
                    output_attentions,
                )

            hidden_states = layer_outputs[0]

            if output_attentions:
                all_self_attentions = all_self_attentions + (layer_outputs[1],)

        if output_hidden_states:
            all_hidden_states = all_hidden_states + (hidden_states,)

        if not return_dict:
            return tuple(
                v
                for v in [
                    hidden_states,
                    all_hidden_states,
                    all_self_attentions,
                ]
                if v is not None
            )
        return BaseModelOutputWithPastAndCrossAttentions(
            last_hidden_state=hidden_states,
            hidden_states=all_hidden_states,
            attentions=all_self_attentions,
        )


class QBertPooler(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = nn.Linear(config.hidden_size, config.hidden_size)
        self.activation = nn.Tanh()

    def forward(self, hidden_states: torch.Tensor) -> torch.Tensor:
        # We "pool" the model by simply taking the hidden state corresponding
        # to the first token.
        first_token_tensor = hidden_states[:, 0]
        pooled_output = self.dense(first_token_tensor)
        pooled_output = self.activation(pooled_output)
        return pooled_output


class QBertPredictionHeadTransform(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = nn.Linear(config.hidden_size, config.hidden_size)
        if isinstance(config.hidden_act, str):
            self.transform_act_fn = ACT2FN[config.hidden_act]
        else:
            self.transform_act_fn = config.hidden_act
        self.LayerNorm = nn.LayerNorm(config.hidden_size, eps=config.layer_norm_eps)

    def forward(self, hidden_states: torch.Tensor) -> torch.Tensor:
        hidden_states = self.dense(hidden_states)
        hidden_states = self.transform_act_fn(hidden_states)
        hidden_states = self.LayerNorm(hidden_states)
        return hidden_states


class QBertLMPredictionHead(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.transform = QBertPredictionHeadTransform(config)

        # The output weights are the same as the input embeddings, but there is
        # an output-only bias for each token.
        self.decoder = nn.Linear(config.hidden_size, config.vocab_size, bias=False)

        self.bias = nn.Parameter(torch.zeros(config.vocab_size))

        # Need a link between the two variables so that the bias is correctly resized with `resize_token_embeddings`
        self.decoder.bias = self.bias

    def _tie_weights(self):
        self.decoder.bias = self.bias

    def forward(self, hidden_states):
        hidden_states = self.transform(hidden_states)
        hidden_states = self.decoder(hidden_states)
        return hidden_states


class QBertOnlyMLMHead(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.predictions = QBertLMPredictionHead(config)

    def forward(self, sequence_output: torch.Tensor) -> torch.Tensor:
        prediction_scores = self.predictions(sequence_output)
        return prediction_scores


class QBertOnlyNSPHead(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.seq_relationship = nn.Linear(config.hidden_size, 2)

    def forward(self, pooled_output):
        seq_relationship_score = self.seq_relationship(pooled_output)
        return seq_relationship_score


class QBertPreTrainingHeads(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.predictions = QBertLMPredictionHead(config)
        self.seq_relationship = nn.Linear(config.hidden_size, 2)

    def forward(self, sequence_output, pooled_output):
        prediction_scores = self.predictions(sequence_output)
        seq_relationship_score = self.seq_relationship(pooled_output)
        return prediction_scores, seq_relationship_score


class QBertPreTrainedModel(PreTrainedModel):
    """
    An abstract class to handle weights initialization and a simple interface for downloading and loading pretrained
    models.
    """

    config_class = QBertConfig
    base_model_prefix = "qbert"
    supports_gradient_checkpointing = True
    _supports_sdpa = False

    def _init_weights(self, module):
        """Initialize the weights"""
        if isinstance(module, nn.Linear):
            # Slightly different from the TF version which uses truncated_normal for initialization
            # cf https://github.com/pytorch/pytorch/pull/5617
            module.weight.data.normal_(mean=0.0, std=self.config.initializer_range)
            if module.bias is not None:
                module.bias.data.zero_()
        elif isinstance(module, nn.Embedding):
            module.weight.data.normal_(mean=0.0, std=self.config.initializer_range)
            if module.padding_idx is not None:
                module.weight.data[module.padding_idx].zero_()
        elif isinstance(module, nn.LayerNorm):
            module.bias.data.zero_()
            module.weight.data.fill_(1.0)
        elif isinstance(module, QuaternionTransformation):
            nn.init.xavier_uniform_(module.r_weight)
            nn.init.xavier_uniform_(module.i_weight)
            nn.init.xavier_uniform_(module.j_weight)
            nn.init.xavier_uniform_(module.k_weight)


